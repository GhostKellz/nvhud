test "font metrics" {
    try std.testing.expectEqual(@as(u32, 8), char_width);
    try std.testing.expectEqual(@as(u32, 16), char_height);
    try std.testing.expectEqual(@as(u32, 95), char_count);
    try std.testing.expectEqual(@as(u32, 128), texture_width);
    try std.testing.expectEqual(@as(u32, 112), texture_height); // 6 rows × 16 = 96, but we have 95 chars
}

test "getCharUV space" {
    const uv = getCharUV(' ');
    try std.testing.expectApproxEqRel(@as(f32, 0.0), uv.u0, 0.001);
    try std.testing.expectApproxEqRel(@as(f32, 0.0), uv.v0, 0.001);
}

test "getCharUV A" {
    const uv = getCharUV('A');
    // 'A' is char 65, index 33 (65-32)
    // col = 33 % 16 = 1, row = 33 / 16 = 2
    const expected_u0 = 1.0 * 8.0 / 128.0; // 0.0625
    const expected_v0 = 2.0 * 16.0 / 112.0; // ~0.286
    try std.testing.expectApproxEqRel(expected_u0, uv.u0, 0.01);
    try std.testing.expectApproxEqRel(expected_v0, uv.v0, 0.01);
}

test "getCharUV invalid" {
    // Invalid chars should return space
    const uv = getCharUV(0);
    const space_uv = getCharUV(' ');
    try std.testing.expectEqual(space_uv.u0, uv.u0);
}

test "font_data size" {
    try std.testing.expectEqual(@as(usize, 95), font_data.len);
