//! Bitmap Font Data
//!
//! 8x16 pixel bitmap font for overlay text rendering.
//! ASCII characters 32-127 (96 characters).
//! Each character is 8 pixels wide, 16 pixels tall.
//! Data is stored as 16 bytes per character (1 bit per pixel, 8 bits per row).

const std = @import("std");

/// Font metrics
pub const char_width: u32 = 8;
pub const char_height: u32 = 16;
pub const first_char: u8 = 32; // Space
pub const last_char: u8 = 126; // Tilde
pub const char_count: u32 = last_char - first_char + 1;

/// Texture dimensions (16 chars per row)
pub const chars_per_row: u32 = 16;
pub const texture_width: u32 = chars_per_row * char_width; // 128
pub const texture_height: u32 = ((char_count + chars_per_row - 1) / chars_per_row) * char_height; // 112

/// Get UV coordinates for a character (normalized 0-1)
pub fn getCharUV(char: u8) struct { u0: f32, v0: f32, u1: f32, v1: f32 } {
    if (char < first_char or char > last_char) {
        // Return space for invalid characters
        return getCharUV(' ');
    }

    const idx = char - first_char;
    const col = idx % chars_per_row;
    const row = idx / chars_per_row;

    const tex_u0 = @as(f32, @floatFromInt(col * char_width)) / @as(f32, @floatFromInt(texture_width));
    const tex_v0 = @as(f32, @floatFromInt(row * char_height)) / @as(f32, @floatFromInt(texture_height));
    const tex_u1 = @as(f32, @floatFromInt((col + 1) * char_width)) / @as(f32, @floatFromInt(texture_width));
    const tex_v1 = @as(f32, @floatFromInt((row + 1) * char_height)) / @as(f32, @floatFromInt(texture_height));

    return .{ .u0 = tex_u0, .v0 = tex_v0, .u1 = tex_u1, .v1 = tex_v1 };
}

