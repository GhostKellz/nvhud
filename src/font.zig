    for (0..char_count) |char_idx| {
        const col = char_idx % chars_per_row;
        const row = char_idx / chars_per_row;

        for (0..char_height) |y| {
            const row_data = font_data[char_idx][y];
            for (0..char_width) |x| {
                const bit = (row_data >> @intCast(7 - x)) & 1;
                const pixel_x = col * char_width + x;
                const pixel_y = row * char_height + y;
                const pixel_idx = (pixel_y * texture_width + pixel_x) * 4;

                if (bit == 1) {
                    texture[pixel_idx + 0] = 255; // R
                    texture[pixel_idx + 1] = 255; // G
                    texture[pixel_idx + 2] = 255; // B
                    texture[pixel_idx + 3] = 255; // A
                } else {
                    texture[pixel_idx + 0] = 0;
                    texture[pixel_idx + 1] = 0;
                    texture[pixel_idx + 2] = 0;
                    texture[pixel_idx + 3] = 0;
                }
            }
        }
    }

    return texture;
}

/// Generate texture data (R8 format - single channel)
/// Returns a 128x112 grayscale texture
pub fn generateTextureR8() [texture_width * texture_height]u8 {
    var texture: [texture_width * texture_height]u8 = undefined;
    @memset(&texture, 0);

    for (0..char_count) |char_idx| {
        const col = char_idx % chars_per_row;
        const row = char_idx / chars_per_row;

        for (0..char_height) |y| {
            const row_data = font_data[char_idx][y];
            for (0..char_width) |x| {
                const bit = (row_data >> @intCast(7 - x)) & 1;
                const pixel_x = col * char_width + x;
                const pixel_y = row * char_height + y;
                const pixel_idx = pixel_y * texture_width + pixel_x;

                texture[pixel_idx] = if (bit == 1) 255 else 0;
            }
        }
    }

    return texture;
}

